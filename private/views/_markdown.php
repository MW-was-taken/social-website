  <h4>Usage</h4>
  <br>
  <h3>Input</h3>
  <label for="before">This is *bold*</label>
  <h3>Output</h3>
  <label for="after">This is <b>bold</b><br></label>
  <br>
  <p>This uses bold text as an example, but this goes for all markdown features.</p>
  <br>
  <h4>Bold Text</h4>
  <p>As previously stated, to use <b>bold text</b>, use the <code>*</code> symbol.</p>
  <h4>Italic Text</h4>
  <p>To use <i>italic text</i>, use the <code>_</code> symbol.</p>
  <h4>Strikethrough Text</h4>
  <p>To use <strike>strikethrough text</strike>, use the <code>%</code> symbol.</p>
  <h4>Code Text</h4>
  <p>To use <code>code text</code>, use the <code>`</code> symbol.</p>
